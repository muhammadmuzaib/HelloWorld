package com.mynameis;

public class MyNameIs
{
    public static void main(String[] args)
    {
        int value = 0;
        int maxLimit = 10;

        //For loop to increment value.
        for(int index= 0; index < maxLimit; index++)
        {
            value++;
            System.out.println("\nIncremented value = " + value);
        }

    }
}
